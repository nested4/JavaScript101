// Introduction to JavaScript Programming

// JavaScript is a high-level, interpreted programming language primarily used for web development.
// It is executed in the browser and provides dynamic functionality to web pages.

// Key Differences with Java:
// 1. JavaScript is a dynamically typed language, whereas Java is statically typed.
// 2. JavaScript uses prototype-based inheritance, while Java uses class-based inheritance.
// 3. JavaScript supports functional programming paradigms along with object-oriented programming.
// 4. JavaScript has a more forgiving syntax and allows for more flexible coding styles compared to Java.

// Let's start with a simple example: printing "Hello, world!" to the console.

console.log("Hello, world!");

// In JavaScript, we use the console.log() function to print output to the console.
// The string "Hello, world!" is enclosed in double quotes, but single quotes can also be used.

// Next, let's explore some basic features of JavaScript.

// Variables:
// JavaScript variables are declared using the 'let' keyword.
// They are dynamically typed, meaning they can hold values of any type.

let message = "Welcome to JavaScript!"; // Declaring a variable and assigning a string value

console.log(message); // Printing the value of the 'message' variable

// Comments:
// Single-line comments start with '//' and can be used to add explanations or clarify code.

/*
   Multi-line comments can span
   multiple lines and are enclosed
   between '/*' and '*\/'.
*/

// Data Types:
// JavaScript has several built-in data types, including:
// - Numbers (e.g., 5, 3.14)
// - Strings (e.g., "Hello", 'World')
// - Booleans (e.g., true, false)
// - Null (represents the absence of any object value)
// - Undefined (represents an uninitialized variable)

// Arithmetic Operations:
// JavaScript supports basic arithmetic operations, such as addition, subtraction, multiplication, division, and modulus.

let num1 = 10;
let num2 = 5;

let sum = num1 + num2;
let difference = num1 - num2;
let product = num1 * num2;
let quotient = num1 / num2;
let remainder = num1 % num2;

console.log(sum, difference, product, quotient, remainder); // Printing the results

// JavaScript provides many more features and functionalities that we will explore in the upcoming files.

// This concludes the introduction to JavaScript programming.
