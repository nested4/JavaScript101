// Escape Sequences

// In this file, we will explore escape sequences in JavaScript.

// Key Differences with Java:
// 1. JavaScript and Java use similar escape sequences for common characters.
// 2. JavaScript supports additional escape sequences for special characters.

// Escape sequences are used to represent characters that cannot be typed directly in a string.

// Common Escape Sequences:
// In JavaScript, we can use the following escape sequences:

// \n: Newline
console.log("Hello\nWorld");

// \t: Tab
console.log("Name:\tJohn");

// \": Double quote
console.log("He said, \"Hello!\"");

// \': Single quote
console.log('It\'s a beautiful day.');

// \\: Backslash
console.log("C:\\path\\to\\file");

// Additional Escape Sequences in JavaScript:
// JavaScript supports some additional escape sequences not found in Java.

// \r: Carriage return
console.log("Hello\rWorld");

// \b: Backspace
console.log("Hello\bWorld");

// \f: Form feed
console.log("Hello\fWorld");

// \v: Vertical tab
console.log("Hello\vWorld");

// Unicode Escape Sequence:
// JavaScript allows using Unicode escape sequences to represent characters beyond the ASCII range.

console.log("\u03A9"); // Output: Î©

// This represents the Greek capital letter Omega.

// Note: Escape sequences are often used in string literals and regular expressions.

// That's all about escape sequences in JavaScript!

