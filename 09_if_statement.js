// If & Else and Multi-Branch If Statements

// In this file, we will explore the if-else statement and multi-branch if statements in JavaScript.

// If-Else Statement:
// The if-else statement allows you to execute different code blocks based on different conditions.

let num = 10;

if (num > 0) {
  console.log("The number is positive");
} else {
  console.log("The number is non-positive");
}

// Multi-Branch If Statements:
// JavaScript allows you to have multiple conditions using else if statements.

let score = 75;

if (score >= 90) {
  console.log("Excellent");
} else if (score >= 80) {
  console.log("Good");
} else if (score >= 70) {
  console.log("Average");
} else {
  console.log("Needs improvement");
}

// The code blocks within the if and else if statements are evaluated in order.
// Once a condition is found to be true, the corresponding code block is executed, and the rest of the conditions are skipped.

